export function AppSidebar({
  ...props
}) {
  const [user, setUser] = useState(null);

  useEffect(() => {
    // Ambil data user dari localStorage
    const storedUser = localStorage.getItem('user');
    const storedGoogleAccount = localStorage.getItem('googleAccount');
    
    if (storedUser) {
      try {
        const parsedUser = JSON.parse(storedUser);
        
        // Jika ada data Google account, merge dengan user data
        if (storedGoogleAccount) {
          const googleData = JSON.parse(storedGoogleAccount);
          
          // Merge data, prioritaskan data dari database
          const mergedUser = {
            ...parsedUser,
            profilePicture: parsedUser.profilePicture || googleData.profilePicture,
            avatar: googleData.profilePicture, // Simpan sebagai avatar fallback
          };
          setUser(mergedUser);
        } else {
          setUser(parsedUser);
        }
      } catch (error) {
        console.error('Error parsing user data:', error);
      }
    }
  }, []);

  return (
    <Sidebar collapsible="icon" {...props}>
      <SidebarHeader>
        <TeamSwitcher teams={data.teams} />
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} />
        <NavProjects projects={data.projects} />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={user} />
      </SidebarFooter>
      <SidebarRail />
    </Sidebar>
  );
}
